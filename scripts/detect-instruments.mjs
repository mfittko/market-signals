#!/usr/bin/env node
/**
 * detect-instruments — validate FXEmpire rate slugs against the live API.
 *
 * Default mode: probe a per-market seed list of slugs, keep the ones that
 * resolve, and write config/instruments.yaml (the known-good catalog).
 *
 * --check mode: read the committed config/instruments.yaml and verify every
 * slug in it still resolves live. Exits non-zero on drift (no write).
 */

import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const LOCALE = 'en';
const BASE = `https://www.fxempire.com/api/v1/${LOCALE}`;

const SEEDS = {
  commodities: ['brent-crude-oil', 'wti-crude-oil', 'natural-gas', 'gold', 'silver', 'platinum'],
  indices: ['spx', 'tech100-usd', 'us30-usd', 'de30-eur', 'uk100-gbp', 'jp225-usd', 'fr40-eur', 'vix'],
  currencies: ['eur-usd', 'usd-jpy', 'gbp-usd', 'usd-chf', 'usd-cad', 'aud-usd', 'nzd-usd'],
  'crypto-coin': ['bitcoin', 'ethereum', 'solana'],
};

const __filename = fileURLToPath(import.meta.url);
const REPO_ROOT = path.resolve(path.dirname(__filename), '..');
const CONFIG_PATH = path.join(REPO_ROOT, 'config', 'instruments.yaml');

const USAGE = `detect-instruments — validate FXEmpire rate slugs against the live API.

Usage:
  node scripts/detect-instruments.mjs           write config/instruments.yaml from resolving seeds
  node scripts/detect-instruments.mjs --check    verify committed slugs still resolve (no write)
  node scripts/detect-instruments.mjs --help     show this help (no network)
`;

function ratesUrl(market, slugs) {
  const csv = encodeURIComponent(slugs.join(','));
  if (market === 'currencies') {
    return `${BASE}/currencies/rates?category=&includeSparkLines=false&includeFullData=true&instruments=${csv}`;
  }
  return `${BASE}/${market}/rates?instruments=${csv}&includeFullData=true&includeSparkLines=false`;
}

async function fetchJson(url) {
  // ponytail: 3 tries with fixed 1s/2s backoff; add jitter only if it flakes.
  const attempts = 3;
  let lastErr;
  for (let attempt = 1; attempt <= attempts; attempt++) {
    const controller = new AbortController();
    const timer = setTimeout(() => controller.abort(), 25000);
    try {
      const res = await fetch(url, {
        signal: controller.signal,
        headers: {
          accept: 'application/json,*/*',
          'user-agent': 'Mozilla/5.0 (market-signals; detect-instruments)',
        },
      });
      if (!res.ok) throw new Error(`HTTP ${res.status}`);
      return await res.json();
    } catch (err) {
      lastErr = err;
      if (attempt < attempts) {
        await new Promise((r) => setTimeout(r, attempt * 1000));
      }
    } finally {
      clearTimeout(timer);
    }
  }
  throw new Error(`fetch failed for ${url}: ${String(lastErr?.message ?? lastErr)}`);
}

function resolvesIn(payload, slug) {
  const entity = payload?.entities?.[slug];
  if (entity && typeof entity === 'object' && Object.keys(entity).length > 0) return true;
  const price = payload?.prices?.[slug];
  if (price && price.last != null) return true;
  return false;
}

function displayName(payload, slug) {
  return payload?.entities?.[slug]?.name || slug;
}

async function probeMarket(market, slugs) {
  const payload = await fetchJson(ratesUrl(market, slugs));
  const resolved = [];
  for (const slug of slugs) {
    if (resolvesIn(payload, slug)) {
      resolved.push({ slug, name: displayName(payload, slug) });
    }
  }
  return resolved;
}

function writeYaml(markets) {
  const now = new Date().toISOString();
  const lines = [
    '# Generated by scripts/detect-instruments.mjs — do not edit by hand.',
    '# Known-good instruments validated against the live FXEmpire rates API.',
    `generatedAt: ${now}`,
    'markets:',
  ];
  for (const [market, entries] of Object.entries(markets)) {
    lines.push(`  ${market}:`);
    for (const { slug, name } of entries) {
      lines.push(`    - slug: ${slug}`);
      lines.push(`      name: ${JSON.stringify(name)}`);
      lines.push(`      lastValidated: ${now}`);
    }
  }
  const dir = path.dirname(CONFIG_PATH);
  fs.mkdirSync(dir, { recursive: true });
  fs.writeFileSync(CONFIG_PATH, lines.join('\n') + '\n');
}

// Minimal line parser: group `slug:` values under `  <market>:` headers.
function parseCommittedSlugs(text) {
  const markets = {};
  let current = null;
  for (const rawLine of text.split('\n')) {
    const marketMatch = rawLine.match(/^ {2}([A-Za-z0-9_-]+):\s*$/);
    if (marketMatch) {
      current = marketMatch[1];
      markets[current] = [];
      continue;
    }
    const slugMatch = rawLine.match(/^ {4}- slug:\s*(\S+)\s*$/);
    if (slugMatch && current) {
      markets[current].push(slugMatch[1]);
    }
  }
  return markets;
}

async function runWrite() {
  const markets = {};
  let hadEmptyMarket = false;
  for (const [market, slugs] of Object.entries(SEEDS)) {
    const resolved = await probeMarket(market, slugs);
    markets[market] = resolved;
    process.stdout.write(`${market}: resolved ${resolved.length}/${slugs.length}\n`);
    if (resolved.length === 0) hadEmptyMarket = true;
  }
  writeYaml(markets);
  process.stdout.write(`wrote ${CONFIG_PATH}\n`);
  if (hadEmptyMarket) {
    process.stderr.write('a market returned zero resolving slugs — likely an API/shape break\n');
    process.exit(1);
  }
}

async function runCheck() {
  if (!fs.existsSync(CONFIG_PATH)) {
    throw new Error(`missing ${CONFIG_PATH} — run without --check first to generate it`);
  }
  const committed = parseCommittedSlugs(fs.readFileSync(CONFIG_PATH, 'utf8'));
  let drift = false;
  for (const [market, slugs] of Object.entries(committed)) {
    if (!slugs.length) continue;
    const resolved = new Set((await probeMarket(market, slugs)).map((e) => e.slug));
    for (const slug of slugs) {
      if (!resolved.has(slug)) {
        drift = true;
        process.stderr.write(`DRIFT ${market}/${slug} no longer resolves\n`);
      }
    }
    process.stdout.write(`${market}: ${resolved.size}/${slugs.length} committed slugs still resolve\n`);
  }
  if (drift) {
    process.stderr.write('instrument catalog drift detected\n');
    process.exit(1);
  }
  process.stdout.write('instrument catalog OK\n');
}

async function main() {
  const argv = process.argv.slice(2);
  if (argv.includes('--help') || argv.includes('-h')) {
    process.stdout.write(USAGE);
    return;
  }
  if (argv.includes('--check')) {
    await runCheck();
    return;
  }
  await runWrite();
}

main().catch((e) => {
  process.stderr.write(`detect-instruments error: ${e.message}\n`);
  process.exit(1);
});
